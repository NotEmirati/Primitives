# return the length of a set
def set_length(set: set):
    return len(set)

# sorts the given set
def sorted_set(givenSet: set):
    return sorted(givenSet)

# return the l
def sum_set(givenSet: set):
    return sum(givenSet)

# return the smallest element of a set. Can be used for any iterable, including several iterables, can have optional args: 'key', 'iterables', 'default'.
def min_value(set: set):
    return min(set)
